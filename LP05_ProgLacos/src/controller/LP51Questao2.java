public class LP51Questao2 {

	/* Desenvolver um programa que exiba os números em ordem decrescente de 100 até 1.
	 * 
	 */
		
	public static void main(String[] args) {
		
	int cont = 100;
		
	while ( cont >= 1) {
	
	System.out.println(cont);
	cont = cont - 1;
		}
			
	}

}
